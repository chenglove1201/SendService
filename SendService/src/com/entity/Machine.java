package com.entity;

public class Machine {
	private int id;
	private String cpu_id;
	private String bind_mch_type;
	private String bind_beauty;
	private String cooperation_mode;
	private String into_date;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getCpu_id() {
		return cpu_id;
	}

	public void setCpu_id(String cpu_id) {
		this.cpu_id = cpu_id;
	}

	public String getBind_mch_type() {
		return bind_mch_type;
	}

	public void setBind_mch_type(String bind_mch_type) {
		this.bind_mch_type = bind_mch_type;
	}

	public String getBind_beauty() {
		return bind_beauty;
	}

	public void setBind_beauty(String bind_beauty) {
		this.bind_beauty = bind_beauty;
	}

	public String getCooperation_mode() {
		return cooperation_mode;
	}

	public void setCooperation_mode(String cooperation_mode) {
		this.cooperation_mode = cooperation_mode;
	}

	public String getInto_date() {
		return into_date;
	}

	public void setInto_date(String into_date) {
		this.into_date = into_date;
	}

}
